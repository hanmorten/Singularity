/**
 * <p>The Singularity Machine Learning Tutorial provides a range of basic
 * machine learning algorithm implementations for you to explore.</p>
 * <p>A good starting point if you are new to Machine Learning are
 * Classifiers, as both the basic concept and implementation are generally
 * simple.</p> 
 * <ul>
 *   <li>
 *     {@link org.singularity.algorithms.classifier Classifiers} are used to
 *     make binary true/false predictions.
 *   </li>
 *   <li>
 *     {@link org.singularity.algorithms.kernel Kernel} functions are used to
 *     make predictions on data that is not linearly separable.
 *   </li>
 *   <li>
 *     {@link org.singularity.algorithms.regression Regression} algorithms
 *     are used to make a real-value prediction, and overlap with traditional
 *     statistical analysis.
 *   </li>
 *   <li>
 *     {@link org.singularity.algorithms.clustering Clustering} algorithms
 *     are used to find patterns in data or to generate values for missing
 *     data-points. Clustering is used to extract knowledge from data, and
 *     to make forecasts/predictions for your data.
 *   </li>
 *   <li>
 *     {@link org.singularity.algorithms.ranking Ranking} algorithms
 *     are used to find sort data based on an order of preference/fit.
 *     Examples include sorting web pages, articles or products based on
 *     what a reader/consumer is most likely to be interested in.
 *   </li>
 *   <li>
 *     {@link org.singularity.algorithms.neuralnet Neural Networks} are
 *     used for large and complex classification tasks, such as image or
 *     voice recognition.
 *   </li>
 * </ul>
 * 
 * <p>This tutorial also provides a small set of applications of the
 * various contained learning algorithms. These applications include:</p>
 * <ul>
 *   <li>
 *     A {@link org.singularity.application.tictactoe Tic-Tac-Toe} game
 *     that can be played between any combination of humans (you),
 *     a learning algorithm or an algorithm that implements the perfect
 *     tic-tac-toe strategy.
 *   </li>
 *   <li>
 *     A sample {@link org.singularity.application.forecast forecasting}
 *     implementation that provides a demand-curve for a sample product
 *     at any point in time.
 *   </li>
 *   <li>
 *      A {@link org.singularity.application.calendar calendar} predictor,
 *      that estimates number of products sold for any future date, based on
 *      sales data from the past.
 *    </li>
 *    <li>
 *      An {@link org.singularity.application.mnist handwriting recognition}
 *      implementation, using the MNIST data-set for scanned handwriting images.
 *    </li>
 *  </ul>
 */
package org.singularity.algorithms;